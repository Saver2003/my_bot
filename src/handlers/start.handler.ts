import { Scenes, Telegraf } from 'telegraf';
import { startKeyboard, segmentKeyboard, beginnerKeyboard, advancedKeyboard } from '../utils/keyboards';

export function registerStartHandler(bot: Telegraf<Scenes.WizardContext>): void {
  bot.start(async (ctx) => {
    await ctx.replyWithHTML(
      '🧠 <b>Хочешь систему, которая организует твою жизнь за 10 минут?</b>\n\n' +
        'Этот Notion шаблон поможет тебе навести порядок в задачах, целях и проектах — без лишних усилий.\n\n' +
        '<b>Что внутри:</b>\n' +
        '• Трекер задач и привычек\n' +
        '• Планировщик недели и месяца\n' +
        '• База проектов и целей\n' +
        '• Готовые страницы для быстрого старта\n\n' +
        '👇 Нажми ниже, чтобы посмотреть',
      startKeyboard(),
    );
  });

  bot.action('view_template', async (ctx) => {
    await ctx.answerCbQuery();
    await ctx.replyWithHTML('🚀 <b>Шаблон</b>\n\nСсылка на шаблон: <i>скоро будет доступна</i>');
  });

  bot.action('how_it_works', async (ctx) => {
    await ctx.answerCbQuery();
    await ctx.replyWithHTML(
      '📖 <b>Как это работает</b>\n\n' +
        '<b>Что такое Notion?</b>\n' +
        'Notion — это универсальное приложение для заметок, задач и проектов. Всё в одном месте, работает на любом устройстве.\n\n' +
        '<b>Кому подходит:</b>\n' +
        '• Тем, кто теряется в задачах и не знает, с чего начать\n' +
        '• Тем, кто уже пробовал другие инструменты, но они не прижились\n' +
        '• Всем, кто хочет видеть свои цели и дела в одном месте\n\n' +
        '<b>Какой результат:</b>\n' +
        '✅ Ясность — ты знаешь, что делать сегодня\n' +
        '✅ Спокойствие — ничего не забыто и не потеряно\n' +
        '✅ Прогресс — видишь, как двигаешься к целям\n\n' +
        '👇 Выбери, что подходит тебе:',
      segmentKeyboard(),
    );
  });

  bot.action('segment_beginner', async (ctx) => {
    await ctx.answerCbQuery();
    await ctx.replyWithHTML(
      '🌱 <b>Отлично, начнём с нуля!</b>\n\n' +
        'Notion — это как блокнот, планировщик и база данных в одном приложении. Звучит сложно, но мы всё сделали за тебя.\n\n' +
        '<b>Что тебе не нужно делать:</b>\n' +
        '❌ Разбираться в настройках\n' +
        '❌ Создавать страницы с нуля\n' +
        '❌ Гуглить «как это работает»\n\n' +
        '<b>Что тебе нужно сделать:</b>\n' +
        '✅ Скопировать шаблон одним кликом\n' +
        '✅ Вписать свои задачи и цели\n' +
        '✅ Пользоваться — всё уже настроено\n\n' +
        '💡 <i>Первые пользователи осваивают шаблон за 10 минут без инструкций.</i>\n\n' +
        '👇 Готов попробовать?',
      beginnerKeyboard(),
    );
  });

  bot.action('segment_advanced', async (ctx) => {
    await ctx.answerCbQuery();
    await ctx.replyWithHTML(
      '⚡️ <b>Уже в теме — тогда сразу к делу</b>\n\n' +
        'Этот шаблон не для демонстрации возможностей Notion. Он заточен под реальную работу.\n\n' +
        '<b>Что внутри:</b>\n' +
        '🗂 Связанные базы данных — задачи, проекты, цели в единой системе\n' +
        '🔍 Готовые фильтры и представления — не нужно настраивать вручную\n' +
        '📐 Проверенная архитектура — структура, которая не рассыпается через неделю\n' +
        '⚙️ Лёгкая адаптация — всё логично названо и разделено\n\n' +
        '<b>Что получаешь:</b>\n' +
        '⏱ Экономишь 3–5 часов на построении системы с нуля\n' +
        '📈 Видишь прогресс по целям в реальном времени\n' +
        '🧠 Освобождаешь голову — система держит всё вместо тебя\n\n' +
        '👇 Посмотришь?',
      advancedKeyboard(),
    );
  });
}
